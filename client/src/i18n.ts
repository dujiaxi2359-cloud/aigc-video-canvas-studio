function normalizeLocale(input?: string | null): Locale {
  const value = (input || "").toLowerCase();
  if (value === "en" || value.startsWith("en-")) return "en";
  if (value === "zh-tw" || value === "zh-hk" || value.includes("hant")) return "zh-TW";
  return "zh-CN";
}

function initialLocale(): Locale {
  if (typeof window === "undefined") return "zh-CN";
  return normalizeLocale(window.localStorage.getItem(STORAGE_KEY) || window.navigator.language);
}

function interpolate(template: string, vars?: Record<string, string | number | undefined | null>) {
  if (!vars) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => {
    const value = vars[key];
    return value === undefined || value === null ? "" : String(value);
  });
}

export const useI18nStore = create<{
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: string, vars?: Record<string, string | number | undefined | null>) => string;
}>((set, get) => ({
  locale: initialLocale(),
  setLocale(locale) {
    if (typeof window !== "undefined") window.localStorage.setItem(STORAGE_KEY, locale);
    set({ locale });
  },
  t(key, vars) {
    const dict = dictionaries[get().locale] || zhCN;
    return interpolate(dict[key] || zhCN[key] || key, vars);
  }
}));
